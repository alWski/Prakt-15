def odd_list(a, n):
    if n == 0:
        return []
    
    result = odd_list(a, n-1)
    
    if a[n-1] % 2 == 0:
        result.append(a[n-1])
    
    return result
    
def main():
    n = int(input())
    a = [int(input()) for i in range(n)]
    print(odd_list(a, n))

if __name__ == "__main__":
    main()
